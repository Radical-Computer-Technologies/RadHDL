      sleep         : in  std_logic;
      -- Clock for the associated synchronous logic and handshake domain.
      wr_clk        : in  std_logic;
      -- Wr en interface signal.
      wr_en         : in  std_logic
    );
  end component;

  type mm2s_state_t is (MM2S_IDLE, MM2S_AR, MM2S_R);
  type s2mm_state_t is (S2MM_IDLE, S2MM_AW_W, S2MM_B);

  constant AXI_BYTES        : natural := AXI_DATA_WIDTH / 8;
  constant MM_FIFO_WIDTH    : natural := AXIS_DATA_WIDTH + 1;
  constant S2_FIFO_WIDTH    : natural := AXIS_DATA_WIDTH + 1;

  signal control_r       : std_logic_vector(REG_DATA_WIDTH - 1 downto 0) := (others => '0');
  signal status_r        : std_logic_vector(REG_DATA_WIDTH - 1 downto 0) := (others => '0');
  signal mm2s_base_r     : unsigned(AXI_ADDR_WIDTH - 1 downto 0) := (others => '0');
  signal mm2s_end_r      : unsigned(AXI_ADDR_WIDTH - 1 downto 0) := (others => '0');
  signal mm2s_addr_r     : unsigned(AXI_ADDR_WIDTH - 1 downto 0) := (others => '0');
  signal mm2s_len_r      : unsigned(31 downto 0) := (others => '0');
  signal mm2s_rem_r      : unsigned(31 downto 0) := (others => '0');
  signal s2mm_base_r     : unsigned(AXI_ADDR_WIDTH - 1 downto 0) := (others => '0');
  signal s2mm_end_r      : unsigned(AXI_ADDR_WIDTH - 1 downto 0) := (others => '0');
  signal s2mm_addr_r     : unsigned(AXI_ADDR_WIDTH - 1 downto 0) := (others => '0');
  signal s2mm_len_r      : unsigned(31 downto 0) := (others => '0');
  signal s2mm_rem_r      : unsigned(31 downto 0) := (others => '0');
  signal mm2s_state      : mm2s_state_t := MM2S_IDLE;
  signal s2mm_state      : s2mm_state_t := S2MM_IDLE;

  signal rd_data_r       : std_logic_vector(REG_DATA_WIDTH - 1 downto 0) := (others => '0');
  signal wr_valid_r      : std_logic := '0';
  signal rd_valid_r      : std_logic := '0';
  signal error_r         : std_logic := '0';
  signal irq_r           : std_logic := '0';

  signal awaddr_r        : std_logic_vector(AXI_ADDR_WIDTH - 1 downto 0) := (others => '0');
  signal araddr_r        : std_logic_vector(AXI_ADDR_WIDTH - 1 downto 0) := (others => '0');
  signal awvalid_r       : std_logic := '0';
  signal wvalid_r        : std_logic := '0';
  signal bready_r        : std_logic := '0';
  signal arvalid_r       : std_logic := '0';
  signal rready_r        : std_logic := '0';
  signal wdata_r         : std_logic_vector(AXI_DATA_WIDTH - 1 downto 0) := (others => '0');

  signal mm_fifo_din     : std_logic_vector(MM_FIFO_WIDTH - 1 downto 0) := (others => '0');
  signal mm_fifo_dout    : std_logic_vector(MM_FIFO_WIDTH - 1 downto 0);
  signal mm_fifo_wr_en   : std_logic := '0';
  signal mm_fifo_rd_en   : std_logic := '0';
  signal mm_fifo_empty   : std_logic;
  signal mm_fifo_full    : std_logic;
  signal mm_fifo_valid   : std_logic;
  signal mm_fifo_rst     : std_logic;
  signal mm_word_valid   : std_logic := '0';
  signal mm_word_data    : std_logic_vector(AXIS_DATA_WIDTH - 1 downto 0) := (others => '0');
  signal mm_word_last    : std_logic := '0';
  signal mm_wait_data    : std_logic := '0';

  signal s2_fifo_din     : std_logic_vector(S2_FIFO_WIDTH - 1 downto 0);
  signal s2_fifo_dout    : std_logic_vector(S2_FIFO_WIDTH - 1 downto 0);
  signal s2_fifo_wr_en   : std_logic;
  signal s2_fifo_rd_en   : std_logic := '0';
  signal s2_fifo_empty   : std_logic;
  signal s2_fifo_full    : std_logic;
  signal s2_fifo_valid   : std_logic;
  signal s2_fifo_rst     : std_logic;
  signal s2_word_valid   : std_logic := '0';
  signal s2_word_data    : std_logic_vector(AXIS_DATA_WIDTH - 1 downto 0) := (others => '0');
  signal s2_word_last    : std_logic := '0';
  signal s2_wait_data    : std_logic := '0';
  signal s2_aw_done      : std_logic := '0';
  signal s2_w_done       : std_logic := '0';

  function ceil_log2(n : positive) return natural is
    variable v : natural := n - 1;
    variable r : natural := 0;
  begin
    while v > 0 loop
      v := v / 2;
      r := r + 1;
    end loop;
    return r;
  end function;

  function fifo_count_width(depth : positive) return natural is
  begin
    return ceil_log2(depth) + 1;
  end function;

  function fifo_read_mode return string is
  begin
    if FIFO_FWFT then
      return "fwft";
    end if;
    return "std";
  end function;

  function fifo_read_latency return integer is
  begin
    if FIFO_FWFT then
      return 0;
    end if;
    return 1;
  end function;

  function axi_size return std_logic_vector is
  begin
    case AXI_BYTES is
      when 1 => return "000";
      when 2 => return "001";
      when 4 => return "010";
      when 8 => return "011";
      when 16 => return "100";
      when 32 => return "101";
      when 64 => return "110";
      when others => return "010";
    end case;
  end function;

  function reg_index(a : std_logic_vector) return natural is
  begin
    return to_integer(shift_right(unsigned(a), 2));
  end function;

  function set_addr_low(old_addr : unsigned; data : std_logic_vector) return unsigned is
    variable outv : unsigned(old_addr'range) := old_addr;
  begin
    if old_addr'length >= data'length then
      outv(data'length - 1 downto 0) := unsigned(data);
    else
      outv := unsigned(data(old_addr'length - 1 downto 0));
    end if;
    return outv;
  end function;

  function set_addr_high(old_addr : unsigned; data : std_logic_vector) return unsigned is
    variable outv : unsigned(old_addr'range) := old_addr;
    variable high_width : natural;
  begin
    if old_addr'length > data'length then
      high_width := old_addr'length - data'length;
      if high_width >= data'length then
        outv((2 * data'length) - 1 downto data'length) := unsigned(data);
      else
        outv(old_addr'length - 1 downto data'length) := unsigned(data(high_width - 1 downto 0));
      end if;
    end if;
    return outv;
  end function;

  function addr_low_word(a : unsigned) return std_logic_vector is
    variable outv : std_logic_vector(REG_DATA_WIDTH - 1 downto 0) := (others => '0');
  begin
    if a'length >= REG_DATA_WIDTH then
      outv := std_logic_vector(a(REG_DATA_WIDTH - 1 downto 0));
    else
      outv(a'length - 1 downto 0) := std_logic_vector(a);
    end if;
    return outv;
  end function;

  function addr_high_word(a : unsigned) return std_logic_vector is
    variable outv : std_logic_vector(REG_DATA_WIDTH - 1 downto 0) := (others => '0');
    variable high_width : natural;
  begin
    if a'length > REG_DATA_WIDTH then
      high_width := a'length - REG_DATA_WIDTH;
      if high_width >= REG_DATA_WIDTH then
        outv := std_logic_vector(a((2 * REG_DATA_WIDTH) - 1 downto REG_DATA_WIDTH));
      else
        outv(high_width - 1 downto 0) := std_logic_vector(a(a'length - 1 downto REG_DATA_WIDTH));
      end if;
    end if;
    return outv;
  end function;

  function advance_addr(
    current_addr : unsigned;
    base_addr    : unsigned;
    end_addr     : unsigned;
    circular     : std_logic
  ) return unsigned is
    variable next_addr : unsigned(current_addr'range);
  begin
    next_addr := current_addr + AXI_BYTES;
    if circular = '1' and end_addr > base_addr and next_addr >= end_addr then
      next_addr := base_addr;
    end if;
    return next_addr;
  end function;
begin
