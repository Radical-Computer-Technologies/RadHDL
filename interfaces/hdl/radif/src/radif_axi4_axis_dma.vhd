      if rstn = '0' then
        control_r <= (others => '0');
        status_r <= (others => '0');
        mm2s_base_r <= (others => '0');
        mm2s_end_r <= (others => '0');
        mm2s_addr_r <= (others => '0');
        mm2s_len_r <= (others => '0');
        mm2s_rem_r <= (others => '0');
        s2mm_base_r <= (others => '0');
        s2mm_end_r <= (others => '0');
        s2mm_addr_r <= (others => '0');
        s2mm_len_r <= (others => '0');
        s2mm_rem_r <= (others => '0');
        mm2s_state <= MM2S_IDLE;
        s2mm_state <= S2MM_IDLE;
        awvalid_r <= '0';
        wvalid_r <= '0';
        bready_r <= '0';
        arvalid_r <= '0';
        rready_r <= '0';
        mm_word_valid <= '0';
        mm_wait_data <= '0';
        s2_word_valid <= '0';
        s2_wait_data <= '0';
        s2_aw_done <= '0';
        s2_w_done <= '0';
      else
        status_r(4) <= mm_fifo_empty;
        status_r(5) <= mm_fifo_full;
        status_r(6) <= s2_fifo_empty;
        status_r(7) <= s2_fifo_full;

        if reg_wr_en = '1' then
          wr_valid_r <= '1';
          idx := reg_index(reg_wr_addr);
          case idx is
            when 0 =>
              control_r <= reg_data_in;
              if reg_data_in(0) = '1' and ENABLE_MM2S then
                status_r(0) <= '1';
                status_r(1) <= '0';
                status_r(8) <= '0';
                mm2s_addr_r <= mm2s_base_r;
                mm2s_rem_r <= mm2s_len_r;
              elsif reg_data_in(0) = '0' then
                status_r(0) <= '0';
              end if;

              if reg_data_in(8) = '1' and ENABLE_S2MM then
                status_r(2) <= '1';
                status_r(3) <= '0';
                status_r(10) <= '0';
                s2mm_addr_r <= s2mm_base_r;
                s2mm_rem_r <= s2mm_len_r;
              elsif reg_data_in(8) = '0' then
                status_r(2) <= '0';
              end if;
            when 1 =>
              status_r <= status_r and not reg_data_in;
            when 2 =>
              mm2s_base_r <= set_addr_low(mm2s_base_r, reg_data_in);
            when 3 =>
              mm2s_base_r <= set_addr_high(mm2s_base_r, reg_data_in);
            when 4 =>
              mm2s_end_r <= set_addr_low(mm2s_end_r, reg_data_in);
            when 5 =>
              mm2s_end_r <= set_addr_high(mm2s_end_r, reg_data_in);
            when 6 =>
              mm2s_len_r <= unsigned(reg_data_in(31 downto 0));
            when 7 =>
              s2mm_base_r <= set_addr_low(s2mm_base_r, reg_data_in);
            when 8 =>
              s2mm_base_r <= set_addr_high(s2mm_base_r, reg_data_in);
            when 9 =>
              s2mm_end_r <= set_addr_low(s2mm_end_r, reg_data_in);
            when 10 =>
              s2mm_end_r <= set_addr_high(s2mm_end_r, reg_data_in);
            when 11 =>
              s2mm_len_r <= unsigned(reg_data_in(31 downto 0));
            when others =>
              error_r <= '1';
          end case;
        end if;

        if reg_rd_en = '1' then
          rd_valid_r <= '1';
          rd_data_r <= (others => '0');
          idx := reg_index(reg_rd_addr);
          case idx is
            when 0 => rd_data_r <= control_r;
            when 1 => rd_data_r <= status_r;
            when 2 => rd_data_r <= addr_low_word(mm2s_base_r);
            when 3 => rd_data_r <= addr_high_word(mm2s_base_r);
            when 4 => rd_data_r <= addr_low_word(mm2s_end_r);
            when 5 => rd_data_r <= addr_high_word(mm2s_end_r);
            when 6 => rd_data_r(31 downto 0) <= std_logic_vector(mm2s_len_r);
            when 7 => rd_data_r <= addr_low_word(s2mm_base_r);
            when 8 => rd_data_r <= addr_high_word(s2mm_base_r);
            when 9 => rd_data_r <= addr_low_word(s2mm_end_r);
            when 10 => rd_data_r <= addr_high_word(s2mm_end_r);
            when 11 => rd_data_r(31 downto 0) <= std_logic_vector(s2mm_len_r);
            when 12 => rd_data_r <= addr_low_word(mm2s_addr_r);
            when 13 => rd_data_r <= addr_low_word(s2mm_addr_r);
            when others => error_r <= '1';
          end case;
        end if;

        if mm_word_valid = '1' then
          if m_axis_tready = '1' then
            mm_word_valid <= '0';
          end if;
        elsif FIFO_FWFT and mm_fifo_empty = '0' then
          mm_word_last <= mm_fifo_dout(MM_FIFO_WIDTH - 1);
          mm_word_data <= mm_fifo_dout(AXIS_DATA_WIDTH - 1 downto 0);
          mm_word_valid <= '1';
          mm_fifo_rd_en <= '1';
        elsif (not FIFO_FWFT) and mm_wait_data = '0' and mm_fifo_empty = '0' then
          mm_fifo_rd_en <= '1';
          mm_wait_data <= '1';
        elsif (not FIFO_FWFT) and mm_wait_data = '1' and mm_fifo_valid = '1' then
          mm_word_last <= mm_fifo_dout(MM_FIFO_WIDTH - 1);
          mm_word_data <= mm_fifo_dout(AXIS_DATA_WIDTH - 1 downto 0);
          mm_word_valid <= '1';
          mm_wait_data <= '0';
        end if;

        if s2_word_valid = '0' and FIFO_FWFT and s2_fifo_empty = '0' then
          s2_word_last <= s2_fifo_dout(S2_FIFO_WIDTH - 1);
          s2_word_data <= s2_fifo_dout(AXIS_DATA_WIDTH - 1 downto 0);
          s2_word_valid <= '1';
          s2_fifo_rd_en <= '1';
        elsif s2_word_valid = '0' and (not FIFO_FWFT) and s2_wait_data = '0' and s2_fifo_empty = '0' then
          s2_fifo_rd_en <= '1';
          s2_wait_data <= '1';
        elsif s2_word_valid = '0' and (not FIFO_FWFT) and s2_wait_data = '1' and s2_fifo_valid = '1' then
          s2_word_last <= s2_fifo_dout(S2_FIFO_WIDTH - 1);
          s2_word_data <= s2_fifo_dout(AXIS_DATA_WIDTH - 1 downto 0);
          s2_word_valid <= '1';
          s2_wait_data <= '0';
        end if;

        case mm2s_state is
          when MM2S_IDLE =>
            if ENABLE_MM2S and status_r(0) = '1' and mm_fifo_full = '0' and (control_r(1) = '1' or mm2s_rem_r /= 0) then
              araddr_r <= std_logic_vector(mm2s_addr_r);
              arvalid_r <= '1';
              mm2s_state <= MM2S_AR;
            end if;

          when MM2S_AR =>
            if m_axi_arready = '1' then
              arvalid_r <= '0';
              rready_r <= '1';
              mm2s_state <= MM2S_R;
            end if;

          when MM2S_R =>
            rready_r <= not mm_fifo_full;
            if m_axi_rvalid = '1' and mm_fifo_full = '0' then
              mm_fifo_din <= '0' & m_axi_rdata(AXIS_DATA_WIDTH - 1 downto 0);
              if control_r(1) = '0' and mm2s_rem_r <= AXI_BYTES then
                mm_fifo_din(MM_FIFO_WIDTH - 1) <= '1';
              end if;
              mm_fifo_wr_en <= '1';
              rready_r <= '0';

              if m_axi_rresp /= "00" then
                status_r(8) <= '1';
              end if;

              mm2s_addr_r <= advance_addr(mm2s_addr_r, mm2s_base_r, mm2s_end_r, control_r(1));
              if control_r(1) = '0' then
                if mm2s_rem_r <= AXI_BYTES then
                  status_r(0) <= '0';
                  status_r(1) <= '1';
                  mm2s_rem_r <= (others => '0');
                else
                  mm2s_rem_r <= mm2s_rem_r - AXI_BYTES;
                end if;
              end if;
              mm2s_state <= MM2S_IDLE;
            end if;
        end case;

        case s2mm_state is
          when S2MM_IDLE =>
            if ENABLE_S2MM and status_r(2) = '1' and s2_word_valid = '1' and (control_r(9) = '1' or s2mm_rem_r /= 0) then
              awaddr_r <= std_logic_vector(s2mm_addr_r);
              wdata_r <= s2_word_data;
              awvalid_r <= '1';
              wvalid_r <= '1';
              s2_aw_done <= '0';
              s2_w_done <= '0';
              s2mm_state <= S2MM_AW_W;
            end if;

          when S2MM_AW_W =>
            if awvalid_r = '1' and m_axi_awready = '1' then
              awvalid_r <= '0';
              s2_aw_done <= '1';
            end if;
            if wvalid_r = '1' and m_axi_wready = '1' then
              wvalid_r <= '0';
              s2_w_done <= '1';
            end if;
            if (s2_aw_done = '1' or (awvalid_r = '1' and m_axi_awready = '1')) and
               (s2_w_done = '1' or (wvalid_r = '1' and m_axi_wready = '1')) then
              bready_r <= '1';
              s2_word_valid <= '0';
              s2mm_state <= S2MM_B;
            end if;

          when S2MM_B =>
            if m_axi_bvalid = '1' then
              bready_r <= '0';
              if m_axi_bresp /= "00" then
                status_r(10) <= '1';
              end if;

              s2mm_addr_r <= advance_addr(s2mm_addr_r, s2mm_base_r, s2mm_end_r, control_r(9));
              if control_r(9) = '0' then
                if s2mm_rem_r <= AXI_BYTES or s2_word_last = '1' then
                  status_r(2) <= '0';
                  status_r(3) <= '1';
                  s2mm_rem_r <= (others => '0');
                else
                  s2mm_rem_r <= s2mm_rem_r - AXI_BYTES;
                end if;
              end if;
              s2mm_state <= S2MM_IDLE;
            end if;
        end case;
      end if;
    end if;
  end process;
end architecture;
