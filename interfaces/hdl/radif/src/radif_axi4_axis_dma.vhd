  s2_fifo_wr_en <= s_axis_tvalid and s_axis_tready;

  gen_xilinx_vendor : if VENDOR_TAG = "XILINX" generate
  begin
  end generate;

  gen_generic_vendor : if VENDOR_TAG /= "XILINX" generate
  begin
  end generate;

  u_mm2s_fifo : xpm_fifo_sync
    generic map (
      FIFO_MEMORY_TYPE => "auto",
      FIFO_WRITE_DEPTH => FIFO_DEPTH,
      WRITE_DATA_WIDTH => MM_FIFO_WIDTH,
      READ_DATA_WIDTH => MM_FIFO_WIDTH,
      READ_MODE => fifo_read_mode,
      FIFO_READ_LATENCY => fifo_read_latency,
      WR_DATA_COUNT_WIDTH => fifo_count_width(FIFO_DEPTH),
      RD_DATA_COUNT_WIDTH => fifo_count_width(FIFO_DEPTH),
      USE_ADV_FEATURES => "0707"
    )
    port map (
      almost_empty => open,
      almost_full => open,
      data_valid => mm_fifo_valid,
      dbiterr => open,
      dout => mm_fifo_dout,
      empty => mm_fifo_empty,
      full => mm_fifo_full,
      overflow => open,
      prog_empty => open,
      prog_full => open,
      rd_data_count => open,
      rd_rst_busy => open,
      sbiterr => open,
      underflow => open,
      wr_ack => open,
      wr_data_count => open,
      wr_rst_busy => open,
      din => mm_fifo_din,
      injectdbiterr => '0',
      injectsbiterr => '0',
      rd_en => mm_fifo_rd_en,
      rst => mm_fifo_rst,
      sleep => '0',
      wr_clk => clk,
      wr_en => mm_fifo_wr_en
    );

  u_s2mm_fifo : xpm_fifo_sync
    generic map (
      FIFO_MEMORY_TYPE => "auto",
      FIFO_WRITE_DEPTH => FIFO_DEPTH,
      WRITE_DATA_WIDTH => S2_FIFO_WIDTH,
      READ_DATA_WIDTH => S2_FIFO_WIDTH,
      READ_MODE => fifo_read_mode,
      FIFO_READ_LATENCY => fifo_read_latency,
      WR_DATA_COUNT_WIDTH => fifo_count_width(FIFO_DEPTH),
      RD_DATA_COUNT_WIDTH => fifo_count_width(FIFO_DEPTH),
      USE_ADV_FEATURES => "0707"
    )
    port map (
      almost_empty => open,
      almost_full => open,
      data_valid => s2_fifo_valid,
      dbiterr => open,
      dout => s2_fifo_dout,
      empty => s2_fifo_empty,
      full => s2_fifo_full,
      overflow => open,
      prog_empty => open,
      prog_full => open,
      rd_data_count => open,
      rd_rst_busy => open,
      sbiterr => open,
      underflow => open,
      wr_ack => open,
      wr_data_count => open,
      wr_rst_busy => open,
      din => s2_fifo_din,
      injectdbiterr => '0',
      injectsbiterr => '0',
      rd_en => s2_fifo_rd_en,
      rst => s2_fifo_rst,
      sleep => '0',
      wr_clk => clk,
      wr_en => s2_fifo_wr_en
