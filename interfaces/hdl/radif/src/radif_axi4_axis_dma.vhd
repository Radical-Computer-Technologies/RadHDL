  s2_fifo_rst <= not rstn;

  m_axi_awaddr <= awaddr_r;
  m_axi_awlen <= (others => '0');
  m_axi_awsize <= axi_size;
  m_axi_awburst <= "01";
  m_axi_awvalid <= awvalid_r;
  m_axi_wdata <= wdata_r;
  m_axi_wstrb <= (others => '1');
  m_axi_wlast <= '1';
  m_axi_wvalid <= wvalid_r;
  m_axi_bready <= bready_r;
  m_axi_araddr <= araddr_r;
  m_axi_arlen <= (others => '0');
  m_axi_arsize <= axi_size;
  m_axi_arburst <= "01";
  m_axi_arvalid <= arvalid_r;
  m_axi_rready <= rready_r;

  m_axis_tdata <= mm_word_data;
  m_axis_tvalid <= mm_word_valid;
  m_axis_tlast <= mm_word_last;

  s_axis_tready <= '1' when ENABLE_S2MM and status_r(2) = '1' and s2_fifo_full = '0' else '0';
